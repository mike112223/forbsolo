@BACKBONES.register_module
class ResNet(ResNetCls):
    """ResNetEncoder

    Args:
        pretrain(bool)
    """
    def __init__(self, arch, pretrained=True, **kwargs):
        cfg = MODEL_CFGS[arch]
        groups = cfg.get('groups', 1)
        width_per_group = cfg.get('width_per_group', 64)
        super().__init__(
            cfg['block'],
            cfg['layer'],
            groups=groups,
            width_per_group=width_per_group,
            **kwargs
        )

        if pretrained:
            state_dict = load_state_dict_from_url(cfg['weights_url'])
            self.load_state_dict(state_dict, strict=False)

        del self.fc, self.avgpool

    def forward(self, x):
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu(x)
        x = self.maxpool(x)

        outs = []
        for i, layer_name in enumerate(self.res_layers):
            res_layer = getattr(self, layer_name)
            x = res_layer(x)
            if i in self.out_indices:
                outs.append(x)
        return tuple(outs)
